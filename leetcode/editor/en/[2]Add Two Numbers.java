/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution2 {
    public ListNode addTwoNumbers(ListNode l1, ListNode l2) {
        int bal = 0;
        ListNode head = null;
        ListNode last = null;
        while(check(l1, l2, bal)) {
            int sum = 0;
            if (l1 != null) {
                sum = sum + l1.val;
                l1 = l1.next;
            }
            if (l2 != null) {
                sum = sum + l2.val;
                l2 = l2.next;
            }
            sum = sum + bal;
            bal = sum/10;
            ListNode ln = new ListNode(sum % 10);
            if (head == null) {
                head = ln;
                last = ln;
            } else {
                last.next = ln;
                last = last.next;
            }
        }
        return head;
    }

    boolean check(ListNode l1, ListNode l2, int bal) {
        return bal > 0 || l1 != null || l2 != null;
    }
}
